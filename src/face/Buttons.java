package face;

public class Buttons {
	
	public int pressAdd(int num1, int num2){
		int equals;
		equals = num1+num2;
		
		return equals;
	}
	public int pressSubtract(int num1, int num2){
		int equals;
		equals = num1-num2;
		
		return equals;
	}
	public int pressMultiply(int num1, int num2){
		int equals;
		equals = num1*num2;
		return equals;
	}
	public int pressDivide(int num1, int num2){
		int equals;
		equals = num1/num2;
		return equals; 
	}

}
